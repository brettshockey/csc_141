# A number raised to the third power is called a cube. For example, the cube of 2 is written as 2**3 in Python. Make a list of the first 10 cubes (that is, the cube of each integer from 1 to 10), and use a for loop to print out the value of each cube.

cubes = list(range(1, 11))

for cube in cubes:
  print(cube**3)

# Another way:
  
cubes = [1, 8, 27, 64, 125, 216, 343, 512, 729, 1000]

for cube in cubes:
  print(cube)

# Another another way:
  
cubes = []
for value in range(1, 11):
  cubes.append(value**3)

